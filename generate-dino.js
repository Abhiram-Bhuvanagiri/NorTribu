function generateSVGPath(grid) {
  let path = '';
  // Since we want standard CSS size, we can scale by 2.
  // We'll generate a path where each block is 2x2 units.
  for(let y=0; y<grid.length; y++) {
    for(let x=0; x<grid[y].length; x++) {
      if(grid[y][x] === '█') {
        let px = x * 2;
        let py = y * 2;
        path += ' M ' + px + ' ' + py + ' h 2 v 2 h -2 v -2';
      }
    }
  }
  return path.trim();
}

