function generateSVGPath(grid) {
  let path = '';
  for(let y=0; y<grid.length; y++) {
    for(let x=0; x<grid[y].length; x++) {
      if(grid[y][x] === '█') {
        // 1 pixel = 1 unit
        path += ' M ' + x + ' ' + y + ' h 1 v 1 h -1 v -1';
      }
    }
  }
  return path.trim();
}

